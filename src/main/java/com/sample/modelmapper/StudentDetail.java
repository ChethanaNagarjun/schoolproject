package com.sample.modelmapper;

import java.util.UUID;

public class StudentDetail {

	private UUID id;
	private String firstName;
	private String lastName;
	private String gender;
	private String dateOfBirth;
	private Integer age;
	private String streetAddress;
	private String city;
	private String state;
	private Integer zipCode;
	private Integer phoneNo;
	private Integer addmissionYear;
	private Integer rollNo;
	private String teachingStaffId;
	private String nonTeachingStaffId;
	private String reportId;
	private String standardId;
	
	public UUID getId() {
		return id;
	}

	public void setId(UUID id) {
		this.id = id;
	}

	public String getFirstName() {
		return firstName;
	}

	public void setFirstName(String firstName) {
		this.firstName = firstName;
	}

	public String getLastName() {
		return lastName;
	}

	public void setLastName(String lastName) {
		this.lastName = lastName;
	}

	public String getGender() {
		return gender;
	}

	public void setGender(String gender) {
		this.gender = gender;
	}

	public String getDateOfBirth() {
		return dateOfBirth;
	}

	public void setDateOfBirth(String dateOfBirth) {
		this.dateOfBirth = dateOfBirth;
	}

	public Integer getAge() {
		return age;
	}

	public void setAge(Integer age) {
		this.age = age;
	}

	public String getStreetAddress() {
		return streetAddress;
	}

	public void setStreetAddress(String streetAddress) {
		this.streetAddress = streetAddress;
	}

	public String getState() {
		return state;
	}

	public void setState(String state) {
		this.state = state;
	}

	public String getCity() {
		return city;
	}

	public void setCity(String city) {
		this.city = city;
	}

	public Integer getZipCode() {
		return zipCode;
	}

	public void setZipCode(Integer zipCode) {
		this.zipCode = zipCode;
	}

	public Integer getPhoneNo() {
		return phoneNo;
	}

	public void setPhoneNo(Integer phoneNo) {
		this.phoneNo = phoneNo;
	}

	public Integer getAddmissionYear() {
		return addmissionYear;
	}

	public void setAddmissionYear(Integer addmissionYear) {
		this.addmissionYear = addmissionYear;
	}

	public Integer getRollNo() {
		return rollNo;
	}

	public void setRollNo(Integer rollNo) {
		this.rollNo = rollNo;
	}

	public String getTeachingStaffId() {
		return teachingStaffId;
	}

	public void setTeachingStaffId(String teachingStaffId) {
		this.teachingStaffId = teachingStaffId;
	}

	public String getNonTeachingStaffId() {
		return nonTeachingStaffId;
	}

	public void setNonTeachingStaffId(String nonTeachingStaffId) {
		this.nonTeachingStaffId = nonTeachingStaffId;
	}

	public String getReportId() {
		return reportId;
	}

	public void setReportId(String reportId) {
		this.reportId = reportId;
	}

	public String getStandardId() {
		return standardId;
	}

	public void setStandardId(String standardId) {
		this.standardId = standardId;
	}

	
	
	
}
